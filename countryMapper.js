// دالة تحويل رمز الدولة إلى اسم عربي
function getCountryName(countryCode) {
    if (!countryCode) return 'غير محدد';
    
    const upperCode = countryCode.toUpperCase();
    return countryMapper[upperCode] || countryCode;
}

// دالة الحصول على جميع أسماء الدول
function getAllCountryNames() {
    return Object.values(countryMapper);
}

// دالة البحث عن دولة بالاسم العربي
function findCountryByArabicName(arabicName) {
    for (const [code, name] of Object.entries(countryMapper)) {
        if (name === arabicName) {
            return code;
        }
    }
    return null;
}

